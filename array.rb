stack = []

# array.push => add the elements at end of the array
stack.push 1
stack.push 2
stack.push 3

# array.pop => remove the elements at the end of the array
stack.pop
stack.pop
stack.pop

# array.unshift => add the elements at the head of the array
stack.unshift 1
stack.unshift 2
stack.unshift 3

# array.shift => remove the elements at the end of the array
stack.shift
stack.shift
stack.shift
